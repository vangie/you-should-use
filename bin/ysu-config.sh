#!/usr/bin/env bash
# You Should Use — Unified Configuration UI
# Called by shell plugins: ysu config → bin/ysu-config.sh --format {zsh|bash|fish}
# Reads current config from inherited environment variables.
# Writes config file in the appropriate shell format.

set -euo pipefail

# --- Parse arguments ---
_format=""
for arg in "$@"; do
  case "$arg" in
    --format=*) _format="${arg#--format=}" ;;
    --format)   shift_next=1 ;;
    *)
      if [[ "${shift_next:-}" == "1" ]]; then
        _format="$arg"
        shift_next=0
      fi
      ;;
  esac
done

if [[ -z "$_format" ]] || [[ ! "$_format" =~ ^(zsh|bash|fish)$ ]]; then
  echo "Usage: ysu-config.sh --format {zsh|bash|fish}" >&2
  exit 2
fi

# --- Config file path ---
_config_dir="${XDG_CONFIG_HOME:-$HOME/.config}/ysu"
_config_file="$_config_dir/config.$_format"

# --- Defaults (in case not inherited) ---
: "${YSU_REMINDER_ENABLED:=true}"
: "${YSU_SUGGEST_ENABLED:=true}"
: "${YSU_LLM_ENABLED:=false}"
: "${YSU_PROBABILITY:=100}"
: "${YSU_COOLDOWN:=300}"
: "${YSU_LLM_API_URL:=http://localhost:11434/v1/chat/completions}"
: "${YSU_LLM_API_KEY:=}"
: "${YSU_LLM_MODEL:=auto}"
: "${YSU_LLM_MODE:=single}"
: "${YSU_INSTALL_HINT:=true}"
: "${YSU_MESSAGE_FORMAT:={prefix} {arrow} {message}}"
: "${YSU_THEME:=dark}"
: "${YSU_DARK_THEME:=tokyo-night}"
: "${YSU_LIGHT_THEME:=solarized}"

# --- Color initialization ---
_C_RESET=$'\e[0m'

_init_colors() {
  local theme_name
  if [[ "$YSU_THEME" == "light" ]]; then
    theme_name="$YSU_LIGHT_THEME"
  else
    theme_name="$YSU_DARK_THEME"
  fi

  case "$theme_name" in
    tokyo-night)      _C_ARROW=$'\e[1;93m'; _C_HIGHLIGHT=$'\e[1;31m'; _C_COMMAND=$'\e[1;36m'; _C_DIM=$'\e[3m'; _C_HINT=$'\e[1;33m'; _C_OK=$'\e[32m'; _C_ERR=$'\e[31m'; _C_BOLD=$'\e[1m' ;;
    dracula)          _C_ARROW=$'\e[38;5;141m'; _C_HIGHLIGHT=$'\e[1;38;5;212m'; _C_COMMAND=$'\e[38;5;117m'; _C_DIM=$'\e[3;38;5;103m'; _C_HINT=$'\e[38;5;84m'; _C_OK=$'\e[32m'; _C_ERR=$'\e[31m'; _C_BOLD=$'\e[1m' ;;
    monokai)          _C_ARROW=$'\e[38;5;208m'; _C_HIGHLIGHT=$'\e[1;38;5;197m'; _C_COMMAND=$'\e[38;5;148m'; _C_DIM=$'\e[3;38;5;242m'; _C_HINT=$'\e[38;5;186m'; _C_OK=$'\e[32m'; _C_ERR=$'\e[31m'; _C_BOLD=$'\e[1m' ;;
    catppuccin-mocha) _C_ARROW=$'\e[38;5;180m'; _C_HIGHLIGHT=$'\e[1;38;5;211m'; _C_COMMAND=$'\e[38;5;153m'; _C_DIM=$'\e[3;38;5;103m'; _C_HINT=$'\e[38;5;223m'; _C_OK=$'\e[32m'; _C_ERR=$'\e[31m'; _C_BOLD=$'\e[1m' ;;
    solarized)        _C_ARROW=$'\e[1;33m'; _C_HIGHLIGHT=$'\e[1;31m'; _C_COMMAND=$'\e[1;34m'; _C_DIM=$'\e[3;2m'; _C_HINT=$'\e[1;35m'; _C_OK=$'\e[32m'; _C_ERR=$'\e[31m'; _C_BOLD=$'\e[1m' ;;
    catppuccin-latte) _C_ARROW=$'\e[38;5;136m'; _C_HIGHLIGHT=$'\e[1;38;5;124m'; _C_COMMAND=$'\e[38;5;25m'; _C_DIM=$'\e[3;38;5;145m'; _C_HINT=$'\e[38;5;133m'; _C_OK=$'\e[32m'; _C_ERR=$'\e[31m'; _C_BOLD=$'\e[1m' ;;
    github)           _C_ARROW=$'\e[38;5;130m'; _C_HIGHLIGHT=$'\e[1;38;5;124m'; _C_COMMAND=$'\e[38;5;24m'; _C_DIM=$'\e[3;38;5;246m'; _C_HINT=$'\e[38;5;90m'; _C_OK=$'\e[32m'; _C_ERR=$'\e[31m'; _C_BOLD=$'\e[1m' ;;
    *)                _C_ARROW=$'\e[1;93m'; _C_HIGHLIGHT=$'\e[1;31m'; _C_COMMAND=$'\e[1;36m'; _C_DIM=$'\e[3m'; _C_HINT=$'\e[1;33m'; _C_OK=$'\e[32m'; _C_ERR=$'\e[31m'; _C_BOLD=$'\e[1m' ;;
  esac
}

_init_colors

# --- Save config ---
_config_save() {
  mkdir -p "$_config_dir"
  if [[ "$_format" == "fish" ]]; then
    cat > "$_config_file" <<EOF
# You Should Use — Configuration (generated by ysu config)
set -g YSU_REMINDER_ENABLED $YSU_REMINDER_ENABLED
set -g YSU_SUGGEST_ENABLED $YSU_SUGGEST_ENABLED
set -g YSU_LLM_ENABLED $YSU_LLM_ENABLED
set -g YSU_PROBABILITY $YSU_PROBABILITY
set -g YSU_COOLDOWN $YSU_COOLDOWN
set -g YSU_LLM_API_URL "$YSU_LLM_API_URL"
set -g YSU_LLM_API_KEY "$YSU_LLM_API_KEY"
set -g YSU_LLM_MODEL "$YSU_LLM_MODEL"
set -g YSU_LLM_MODE "$YSU_LLM_MODE"
set -g YSU_INSTALL_HINT $YSU_INSTALL_HINT
set -g YSU_MESSAGE_FORMAT "$YSU_MESSAGE_FORMAT"
set -g YSU_THEME "$YSU_THEME"
set -g YSU_DARK_THEME "$YSU_DARK_THEME"
set -g YSU_LIGHT_THEME "$YSU_LIGHT_THEME"
EOF
  else
    cat > "$_config_file" <<EOF
# You Should Use — Configuration (generated by ysu config)
YSU_REMINDER_ENABLED=$YSU_REMINDER_ENABLED
YSU_SUGGEST_ENABLED=$YSU_SUGGEST_ENABLED
YSU_LLM_ENABLED=$YSU_LLM_ENABLED
YSU_PROBABILITY=$YSU_PROBABILITY
YSU_COOLDOWN=$YSU_COOLDOWN
YSU_LLM_API_URL="$YSU_LLM_API_URL"
YSU_LLM_API_KEY="$YSU_LLM_API_KEY"
YSU_LLM_MODEL="$YSU_LLM_MODEL"
YSU_LLM_MODE="$YSU_LLM_MODE"
YSU_INSTALL_HINT=$YSU_INSTALL_HINT
YSU_MESSAGE_FORMAT="$YSU_MESSAGE_FORMAT"
YSU_THEME="$YSU_THEME"
YSU_DARK_THEME="$YSU_DARK_THEME"
YSU_LIGHT_THEME="$YSU_LIGHT_THEME"
EOF
  fi
}

# --- Theme settings sub-menu ---
_config_theme() {
  local _dark_themes=("tokyo-night" "dracula" "monokai" "catppuccin-mocha")
  local _light_themes=("solarized" "catppuccin-latte" "github")
  local _key _cur_theme _i _redraw=0

  while true; do
    [[ "$YSU_THEME" == "light" ]] && _cur_theme="$YSU_LIGHT_THEME" || _cur_theme="$YSU_DARK_THEME"
    if (( _redraw )); then
      printf '\r\e[10A\e[J'
    fi
    _redraw=1
    echo ""
    echo -e "${_C_BOLD}Theme Settings${_C_RESET}"
    echo "━━━━━━━━━━━━━━"
    echo -e "  Mode:   ${_C_BOLD}${YSU_THEME}${_C_RESET}"
    echo -e "  Theme:  ${_C_BOLD}${_cur_theme}${_C_RESET}"
    echo ""
    echo "  Preview:"
    echo -e "  ${_C_DIM}💡 Found alias:${_C_RESET} ${_C_COMMAND}git commit${_C_RESET} ${_C_ARROW}→${_C_RESET} ${_C_HIGHLIGHT}gc${_C_RESET}"
    echo -e "  ${_C_DIM}💡 Modern:${_C_RESET} ${_C_COMMAND}cat${_C_RESET} ${_C_ARROW}→${_C_RESET} ${_C_HIGHLIGHT}bat${_C_RESET} ${_C_HINT}(Syntax-highlighted cat)${_C_RESET}"
    echo ""
    echo -ne "  \e[7m ↑↓/jk \e[0m mode  \e[7m ←→/hl \e[0m theme  \e[7m b \e[0m back  \e[7m q \e[0m quit"

    IFS= read -rsn1 _key
    if [[ "$_key" == $'\e' ]]; then
      read -rsn2 _key
    fi

    case "$_key" in
      '[A'|k|K|'[B'|j|J)
        [[ "$YSU_THEME" == "dark" ]] && YSU_THEME=light || YSU_THEME=dark
        _init_colors
        ;;
      '[C'|l|L)
        if [[ "$YSU_THEME" == "dark" ]]; then
          for _i in "${!_dark_themes[@]}"; do
            [[ "${_dark_themes[$_i]}" == "$YSU_DARK_THEME" ]] && break
          done
          _i=$(( (_i + 1) % ${#_dark_themes[@]} ))
          YSU_DARK_THEME="${_dark_themes[$_i]}"
        else
          for _i in "${!_light_themes[@]}"; do
            [[ "${_light_themes[$_i]}" == "$YSU_LIGHT_THEME" ]] && break
          done
          _i=$(( (_i + 1) % ${#_light_themes[@]} ))
          YSU_LIGHT_THEME="${_light_themes[$_i]}"
        fi
        _init_colors
        ;;
      '[D'|h|H)
        if [[ "$YSU_THEME" == "dark" ]]; then
          for _i in "${!_dark_themes[@]}"; do
            [[ "${_dark_themes[$_i]}" == "$YSU_DARK_THEME" ]] && break
          done
          _i=$(( (_i - 1 + ${#_dark_themes[@]}) % ${#_dark_themes[@]} ))
          YSU_DARK_THEME="${_dark_themes[$_i]}"
        else
          for _i in "${!_light_themes[@]}"; do
            [[ "${_light_themes[$_i]}" == "$YSU_LIGHT_THEME" ]] && break
          done
          _i=$(( (_i - 1 + ${#_light_themes[@]}) % ${#_light_themes[@]} ))
          YSU_LIGHT_THEME="${_light_themes[$_i]}"
        fi
        _init_colors
        ;;
      b|B) echo ""; return 0 ;;
      q|Q) echo ""; return 1 ;;
    esac
  done
}

# --- LLM settings sub-menu ---
_config_llm() {
  local choice
  while true; do
    echo ""
    echo -e "${_C_BOLD}LLM Settings${_C_RESET}"
    echo "━━━━━━━━━━━━"
    echo "  a) API URL:   $YSU_LLM_API_URL"
    echo -e "  b) API Key:   $([[ -n "$YSU_LLM_API_KEY" ]] && echo "••••${YSU_LLM_API_KEY: -4}" || echo '(not set)')"
    echo "  c) Model:     $YSU_LLM_MODEL"
    echo ""
    echo -ne "  \e[7m a-c \e[0m select  \e[7m q \e[0m back: "
    read -r choice

    case "$choice" in
      a) read -erp "  API URL: " -i "$YSU_LLM_API_URL" YSU_LLM_API_URL ;;
      b) read -erp "  API Key: " -i "$YSU_LLM_API_KEY" YSU_LLM_API_KEY ;;
      c) read -erp "  Model: " -i "$YSU_LLM_MODEL" YSU_LLM_MODEL ;;
      q|Q) return ;;
    esac
  done
}

# --- Main config wizard ---
_config_wizard() {
  local choice

  while true; do
    echo ""
    echo -e "${_C_BOLD}You Should Use — Configuration${_C_RESET}"
    echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
    echo -e "  1) Alias Reminders:       $([[ "$YSU_REMINDER_ENABLED" == "true" ]] && echo "${_C_OK}✓ enabled${_C_RESET}" || echo "${_C_ERR}✗ disabled${_C_RESET}")"
    echo -e "  2) Modern Suggestions:    $([[ "$YSU_SUGGEST_ENABLED" == "true" ]] && echo "${_C_OK}✓ enabled${_C_RESET}" || echo "${_C_ERR}✗ disabled${_C_RESET}")"
    echo -e "  3) LLM Suggestions:       $([[ "$YSU_LLM_ENABLED" == "true" ]] && echo "${_C_OK}✓ enabled${_C_RESET}" || echo "${_C_ERR}✗ disabled${_C_RESET}")"
    echo "  4) Tip Probability:       ${YSU_PROBABILITY}%"
    echo "  5) Cooldown:              ${YSU_COOLDOWN}s"
    echo "  6) LLM Settings           →"
    echo "  7) Theme Settings         →"
    echo ""
    echo -ne "  \e[7m 1-7 \e[0m select  \e[7m q \e[0m quit: "
    read -r choice

    case "$choice" in
      1) [[ "$YSU_REMINDER_ENABLED" == "true" ]] && YSU_REMINDER_ENABLED=false || YSU_REMINDER_ENABLED=true ;;
      2) [[ "$YSU_SUGGEST_ENABLED" == "true" ]] && YSU_SUGGEST_ENABLED=false || YSU_SUGGEST_ENABLED=true ;;
      3) [[ "$YSU_LLM_ENABLED" == "true" ]] && YSU_LLM_ENABLED=false || YSU_LLM_ENABLED=true ;;
      4) read -rp "  Probability (1-100): " YSU_PROBABILITY ;;
      5) read -rp "  Cooldown (seconds): " YSU_COOLDOWN ;;
      6) _config_llm ;;
      7) _config_theme || { _config_save; return; } ;;
      q|Q) _config_save; return ;;
      *) continue ;;
    esac
    _config_save
  done
}

# --- Run ---
_config_wizard
